class Converte:
    def animal(a):
        match a:
            case 0:
                return 'vaca'

            case 1:
                return 'galinha'

            case 2:
                return 'ovelha'

    def produto(a):
        match a:
            case 0:
                return "Carne de Vaca"
            case 1:
                return "Carne de Galinha"
            case 2:
                return "Carne de Ovelha"